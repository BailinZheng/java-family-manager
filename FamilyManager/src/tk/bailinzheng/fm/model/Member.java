package tk.bailinzheng.fm.model;

public class Member {
    private String mid;

    private String mamount;

    public Member() {
        this.mid = mid;
        this.mamount = mamount;
    }

    public String getMid() {
        return mid;
    }

    public String getMamount() {
        return mamount;
    }

    public void setMid(String mid) {
        this.mid = mid;
    }

    public void setMamount(String mamount) {
        this.mamount = mamount;
    }
}
