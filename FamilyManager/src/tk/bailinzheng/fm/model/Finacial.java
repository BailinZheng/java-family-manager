package tk.bailinzheng.fm.model;

public class Finacial {

    private String fplace;

    private String fflow;

    private String fthing;

    private String fid;

    public Finacial() {
        this.fplace = fplace;
        this.fflow = fflow;
        this.fthing = fthing;
        this.fid = fid;
    }

    public String getFplace() {
        return fplace;
    }

    public String getFflow() {
        return fflow;
    }

    public String getFthing() {
        return fthing;
    }

    public String getFid() {
        return fid;
    }

    public void setFplace(String fplace) {
        this.fplace = fplace;
    }

    public void setFflow(String fflow) {
        this.fflow = fflow;
    }

    public void setFthing(String fthing) { this.fthing = fthing;}

    public void setFid(String fid) { this.fid = fid;}
}
